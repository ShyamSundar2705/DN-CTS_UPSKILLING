class TypeCasting{
    public static void main(String args[]){

        double a=25.89;
        int b=(int)a;

        System.out.println("Double value = "+a);
        System.out.println("Converted int value = "+b);

        int c=50;
        double d=(double)c;

        System.out.println("Int value = "+c);
        System.out.println("Converted double value = "+d);
    }
}